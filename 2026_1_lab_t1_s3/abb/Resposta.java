public class Resposta {
    private int contador;
    private boolean achou;
    public int getContador() {
        return contador;
    }
    public void incrementaContador() {
        this.contador++;
    }
    public boolean getAchou() {
        return achou;
    }
    public void setAchou(boolean achou) {
        this.achou = achou;
    }
    
}
